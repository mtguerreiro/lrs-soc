void cukControlSfbIntInitialize(void){

}
//-----------------------------------------------------------------------------
int32_t cukControlSfbIntSetParams(void *params, uint32_t n){

    float *p = (float *)params;

    k[0] = *p++;
    k[1] = *p++;
    k[2] = *p++;
    k[3] = *p++;
    ke = *p++;
    dt = *p++;

    xs[0] = *p++;
    xs[1] = *p++;
    xs[2] = *p++;
    xs[3] = *p++;

    us = *p++;

	return 0;
}
//-----------------------------------------------------------------------------
int32_t cukControlSfbIntGetParams(void *in, uint32_t insize, void *out, uint32_t maxoutsize){

    float *p = (float *)out;

    *p++ = k[0];
    *p++ = k[1];
    *p++ = k[2];
    *p++ = k[3];
    *p++ = ke;
    *p++ = dt;

    *p++ = xs[0];
    *p++ = xs[1];
    *p++ = xs[2];
    *p++ = xs[3];

    *p++ = us;

    return 44;
}
//-----------------------------------------------------------------------------
int32_t cukControlSfbIntRun(void *meas, int32_t nmeas, void *refs, int32_t nrefs, void *outputs, int32_t nmaxoutputs){

    cukConfigMeasurements_t *m = (cukConfigMeasurements_t *)meas;
    cukConfigReferences_t *r = (cukConfigReferences_t *)refs;
    cukConfigControl_t *o = (cukConfigControl_t *)outputs;

    static float x1e, x2e, x3e, x4e;
    static float vc;
    static float u, ud;

    vc = m->v_1 + (1.0f / CUK_CONFIG_TF_N2N1) * m->v_2;

    /* Deviations */
    x1e = m->i_1 - xs[0];
    x2e = m->i_2 - xs[1];
    x3e = vc - xs[2];
    x4e = m->v_out - xs[3];

    /* Integral */
    xe = xe_1 + dt * (r->v_o - m->v_out);
    xe_1 = xe;

    /* Control */
    ue = ke * xe;
    ud = k[0] * x1e + k[1] * x2e + k[2] * x3e + k[3] * x4e;
    u = us - ud - ue;

    if( u > 1.0f ) u = 1.0f;
    if( u < 0.0f ) u = 0.0f;

    o->u = u;

    return sizeof(cukConfigControl_t);
}
//-----------------------------------------------------------------------------
void cukControlSfbIntReset(void){

    xe_1 = 0.0f;
}
