static float u = 0.0f;
static float ufinal = 0.5f;
static float uinc = 50.0f / 100000.0f;
//=============================================================================

//=============================================================================
/*-------------------------------- Functions --------------------------------*/
//=============================================================================
//-----------------------------------------------------------------------------
void boostControlStartupInitialize(void){

}
//-----------------------------------------------------------------------------
int32_t boostControlStartupSetParams(void *params, uint32_t n){

    float *p = (float *)params;

    uinc = *p++;
    ufinal = *p++;

	return 0;
}
//-----------------------------------------------------------------------------
int32_t boostControlStartupGetParams(void *in, uint32_t insize, void *out, uint32_t maxoutsize){

    float *p = (float *)out;

    *p++ = uinc;
    *p++ = ufinal;

    return 8;
}
//-----------------------------------------------------------------------------
int32_t boostControlStartupRun(void *meas, int32_t nmeas, void *refs, int32_t nrefs, void *outputs, int32_t nmaxoutputs){

    boostConfigMeasurements_t *m = (boostConfigMeasurements_t *)meas;
    boostConfigReferences_t *r = (boostConfigReferences_t *)refs;
    boostConfigControl_t *o = (boostConfigControl_t *)outputs;

    u += uinc;

    if( u >= ufinal ) u = ufinal;
    if( u <= 0.0f ) u = 0.0f;

    o->u = u;

    return sizeof(boostConfigControl_t);
}
//-----------------------------------------------------------------------------
void boostControlStartupReset(void){

    u = 0.0f;
}
