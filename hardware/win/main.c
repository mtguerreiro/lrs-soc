//=============================================================================
/*-------------------------------- Includes ---------------------------------*/
//=============================================================================
#include <stdint.h>
#include <stdio.h>
#include <stdlib.h>
#include <pthread.h>
#include <unistd.h>

#include <winsock2.h>

#include "threads/opilThread.h"
#include "threads/ocpThread.h"

#include "ocpWin.h"
//=============================================================================

//=============================================================================
/*-------------------------------- Prototypes -------------------------------*/
//=============================================================================
int32_t mainOcpWinInit(void);
//=============================================================================

//=============================================================================
/*---------------------------------- Main -----------------------------------*/
//=============================================================================
//-----------------------------------------------------------------------------
int main(void){

     pthread_t opilThreadHandle, ocpThreadHandle;

     mainOcpWinInit();

    /* Create independent threads each of which will execute function */

     pthread_create( &opilThreadHandle, NULL, opilThread, NULL);
     pthread_create( &ocpThreadHandle, NULL, ocpThread, NULL);

     /* Wait till threads are complete before main continues. Unless we  */
     /* wait we run the risk of executing an exit which will terminate   */
     /* the process and all threads before the threads have completed.   */

     pthread_join( opilThreadHandle, NULL);
     pthread_join( ocpThreadHandle, NULL);

     exit(0);
}
//-----------------------------------------------------------------------------
//=============================================================================

//=============================================================================
/*---------------------------- Static functions -----------------------------*/
//=============================================================================
//-----------------------------------------------------------------------------
int32_t mainOcpWinInit(void){

    ocpWinInitialize( 0 );

    return 0;
}
